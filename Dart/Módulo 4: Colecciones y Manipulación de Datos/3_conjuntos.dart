//Creación y manipulación de conjuntos
/*
Un conjunto en Dart es una colección no ordenada de elementos únicos. A diferencia de las listas, los conjuntos no permiten duplicados.
*/
void main() {
  Set<int> numeros = {1, 2, 3, 4};
  numeros.add(5);
  print(numeros);
}

